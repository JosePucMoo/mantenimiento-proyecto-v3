package com.mantenimiento.test.test_cases.docs.DocsCP_014.newVersion;

public class DocCP_014_2 {
    // Prints a simple message
    public void print(String message) {
        System.out.println(message);
    }

    // Prints a message with a prefix
    public void printWithPrefix(String prefix, String message) {
        System.out.println(prefix + ": " + message);
    }

    // Prints a message in uppercase
    public void printUpperCase(String message) {
        System.out.println(message.toUpperCase());
    }

    // Prints an empty line
    public void printLine() {
        System.out.println();
    }
}
