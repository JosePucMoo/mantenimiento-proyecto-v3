public class Calculator {
    public int add(int a, int b) {
        return a + b;   // línea modificada
    }
    public int subtract(int a, int b) {
        return a - b;
    }
    public int multiply(int a, int b) {  // línea agregada
        return a * b;  // línea agregada
    }
}  // línea agregada
