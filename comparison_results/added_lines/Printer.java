public class Printer {  // línea agregada
    public void print(String message) {  // línea agregada
        System.out.println(message);  // línea agregada
    }  // línea agregada
    public void printWithPrefix(String prefix, String message) {  // línea agregada
        System.out.println(prefix + ": " + message);  // línea agregada
    }  // línea agregada
    public void printUpperCase(String message) {  // línea agregada
        System.out.println(message.toUpperCase());  // línea agregada
    }  // línea agregada
    public void printLine() {  // línea agregada
        System.out.println();  // línea agregada
    }  // línea agregada
}  // línea agregada
